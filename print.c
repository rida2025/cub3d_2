#include "cub3d.h"

void	print_map(char **map)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (map[i])
	{
		while (map[i] && map[i][j])
		{
			printf("%c", map[i][j]);
			j++;
		}
		i++;
		j = 0;
	}
}
